/*================= Increment and Decrement Operators ================
- Operators: ++x, x++, --x, x--
- Pre-increment (++x): increments x first, then returns the new value
- Post-increment (x++): returns the original value, then increments x
- Pre-decrement (--x): decrements x first, then returns the new value
- Post-decrement (x--): returns the original value, then decrements x
*================================================*/

// Pre Increment
 let a = 10;
 let b = ++a;
 console.log(b);
 console.log(a);

// Post Increment
let c = 10;
let d = c++;
console.log(d);
console.log(c);

// Pre Decrement
let e = 10;
let f = --e;
console.log(f);
console.log(e);

// Post Decrement
let g = 10;
let h = g--;
console.log(h);
console.log(g);