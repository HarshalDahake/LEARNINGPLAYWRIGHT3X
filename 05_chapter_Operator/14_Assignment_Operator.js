/*================= Asssignment Operators ================
*  Assignment Operators (=, +=, -=, *=, /=, %=)
*  Assigns a value to a variable. The left side is the variable,
*  the right side is the value. 
*  Compound operators (like +=) perform an operation AND assignment in one step.
=========================================================*/

let x = 10; 
x = "HarshalDahake"; // x is now a string
console.log(x);


let x1 = 10;
x1 += 5; //  x1 = x1 + 5;
console.log(x1);
x1 -= 3; // x1 = x1-3;
console.log(x1);

x *= 2;        // x = x * 2 → 24
x /= 4;        // x = x / 4 → 6
x %= 4;        // x = x % 4 → 2