/*================= String Operators ================
-  String Operators (+, +=)
-  +   → concatenation: joins two strings together
-  +=  → compound concatenation: appends to the existing string
-  console.log with commas (,) logs multiple values separated by spaces
=========================================================*/

let s = "Hi, ";
console.log(typeof s);
s += "Mitansh";
console.log(s);

console.log("Hello" + "World");
console.log("HELLO", "HARSHAL", "Dahake");
console.log(1, 2, 3, 4, "Hello", true);