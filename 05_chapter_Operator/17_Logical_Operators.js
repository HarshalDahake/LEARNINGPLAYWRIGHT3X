/*================= Logical Operators ================
-  Logical Operators (&&, ||, !)
-  Combine or invert boolean values.    
-  && → AND (true only if both are true) i.e. AND Gate
-  || → OR  (true if at least one is true) i.e. OR Gate
-  !  → NOT (flips true ↔ false)
=====================================================*/

let a = true;
let b = false;

console.log(a && b); 
console.log(a || b); 
console.log(!a); 