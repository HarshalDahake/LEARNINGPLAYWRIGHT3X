/* ============= Numbers Literals in JavaScript ==============*/

let count = 42;
let negative = -100;
let zero = 0;

console.log(count, typeof count);
console.log(negative, typeof negative);
console.log(zero, typeof zero);

let h = 0xFF;
let color_hex = 0xFF0000;
console.log(h, typeof h);
console.log(color_hex, typeof color_hex);

let octal = 0o77; //base
let million = 1e6;
let tiny = 1.5e-4;
console.log(octal, typeof octal);
console.log(million, typeof million);
console.log(tiny, typeof tiny);